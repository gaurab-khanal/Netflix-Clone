// QUESTION 1
const targetDiv = document.getElementById('ans1');
const btn = document.getElementById('q1');
btn.addEventListener('click',()=>{
	if(targetDiv.style.display!=="none"){
		targetDiv.style.display = "none";
	}else{
		targetDiv.style.display = "block";
	}
})

